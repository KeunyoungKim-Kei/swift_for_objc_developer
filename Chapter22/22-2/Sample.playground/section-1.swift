func swapInteger16(inout lhs: Int16, inout rhs: Int16) {
    let tmp = lhs
    lhs = rhs
    rhs = tmp
}

func swapFloat(inout lhs: Float, inout rhs: Float) {
    let tmp = lhs
    lhs = rhs
    rhs = tmp
}

func swapDouble(inout lhs: Double, inout rhs: Double) {
    let tmp = lhs
    lhs = rhs
    rhs = tmp
}